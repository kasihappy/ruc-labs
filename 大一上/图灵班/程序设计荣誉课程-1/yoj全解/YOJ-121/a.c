/*
思路：
1. 题目要求计算两个整数的乘积
2. 由于数据范围：-2^32 <= A, B <= 2^31-1
   - 注意这个范围描述可能有误，因为-2^32超过了32位有符号整数的最小值-2^31
   - 但2^31-1是32位有符号整数的最大值
   - 实际上，两个32位整数的乘积可能超过32位，需要使用64位整数来存储
3. 输入格式：两个整数用空格或回车分隔
4. 输出格式：直接输出乘积
5. 使用long long类型（通常是64位）来存储结果，防止溢出

实现步骤：
1. 读取两个整数，使用long long类型存储
2. 计算乘积并存储在long long变量中
3. 使用%lld格式输出结果
*/

#include <stdio.h>

int main() {
    long long a, b;
    // 读取两个整数，它们可能用空格或回车分隔
    scanf("%lld%lld", &a, &b);
    
    // 计算乘积
    long long product = a * b;
    
    // 输出结果
    printf("%lld\n", product);
    
    return 0;
}