"""
程序目的：读取三行不同格式的输入，然后按照指定格式输出。
实现方式：
1. 第一行：使用input读取整行，保留原格式。
2. 第二行：使用input读取整行，然后用split分割成两个字符串，转换为浮点数。
3. 第三行：使用input读取整行，保留原格式。
4. 输出时，第一行原样输出。
5. 输出时，第二行将两个浮点数格式化为保留一位小数，中间用空格隔开。
6. 输出时，第三行原样输出。
"""
# 读取第一行：两个整数，中间用逗号隔开
line1 = input().strip()

# 读取第二行：两个浮点数，中间用空格隔开
x, y = map(float, input().split())

# 读取第三行：两个字母，每个字母前面有空格，中间有逗号
line3 = input().strip()

# 输出第一行：原样输出
print(line1)

# 输出第二行：浮点数保留一位小数
print("{:.1f} {:.1f}".format(x, y))

# 输出第三行：原样输出
print(line3)