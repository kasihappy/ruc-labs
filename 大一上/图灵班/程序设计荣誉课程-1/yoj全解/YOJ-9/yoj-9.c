/*
 * 程序目的：读取三行不同格式的输入，然后按照指定格式输出。
 * 实现方式：
 * 1. 包含标准输入输出头文件 stdio.h，以使用 scanf 和 printf 函数。
 * 2. 定义主函数 main，作为程序入口点。
 * 3. 第一行：读取两个整数，中间有逗号分隔，使用 scanf 的格式化输入 "%d,%d"。
 * 4. 第二行：读取两个浮点数，中间有空格分隔，使用 scanf 的格式化输入 "%lf %lf"。
 * 5. 第三行：读取两个字符，注意每个字符前有空格，中间有逗号，使用 scanf 的格式化输入 " %c, %c"。
 * 6. 输出时，第一行原样输出两个整数，中间加逗号。
 * 7. 输出时，第二行输出两个浮点数，保留一位小数，中间加空格。
 * 8. 输出时，第三行原样输出两个字符，注意每个字符前有空格，中间有逗号。
 * 9. 返回0表示程序正常退出。
 */
#include <stdio.h>

int main() {
    int a, b;
    double x, y;
    char c1, c2;
    
    // 读取第一行：两个整数，中间用逗号隔开
    scanf("%d,%d", &a, &b);
    
    // 读取第二行：两个浮点数，中间用空格隔开
    scanf("%lf %lf", &x, &y);
    
    // 读取第三行：两个字母，每个字母前面有空格，中间有逗号
    scanf(" %c, %c", &c1, &c2);
    
    // 输出第一行：原样输出整数
    printf("%d,%d\n", a, b);
    
    // 输出第二行：浮点数保留一位小数
    printf("%.1lf %.1lf\n", x, y);
    
    // 输出第三行：原样输出字符，注意空格
    printf(" %c, %c\n", c1, c2);
    
    return 0;
}