/*
 * 程序目的：读取一段连续的字符串，然后直接输出。
 * 实现方式：
 * 1. 包含标准输入输出头文件 stdio.h，以使用 scanf 和 printf 函数。
 * 2. 定义主函数 main，作为程序入口点。
 * 3. 声明一个字符数组（字符串缓冲区），大小为101（题目说明长度小于100，加上末尾的'\0'）。
 * 4. 使用 scanf 的 %s 格式读取字符串，它会读取连续的字符直到遇到空白字符。
 * 5. 使用 printf 输出读取的字符串。
 * 6. 返回0表示程序正常退出。
 */
#include <stdio.h>

int main() {
    char str[101];
    scanf("%s", str);
    printf("%s\n", str);
    return 0;
}