/*
 * 程序目的：读取两个整数，比较大小后输出较大的数。
 * 实现方式：
 * 1. 包含标准输入输出头文件 stdio.h，以使用 scanf 和 printf 函数。
 * 2. 定义主函数 main，作为程序入口点。
 * 3. 声明两个整数变量a和b，用于存储输入的两个整数。
 * 4. 使用 scanf 读取两个整数，分别存入a和b。
 * 5. 使用条件运算符（三元运算符）比较a和b的大小：
 *    - 如果a大于b，则输出a
 *    - 否则输出b
 * 6. 返回0表示程序正常退出。
 */
#include <stdio.h>

int main() {
    int a, b;
    scanf("%d %d", &a, &b);
    printf("%d\n", a > b ? a : b);
    return 0;
}